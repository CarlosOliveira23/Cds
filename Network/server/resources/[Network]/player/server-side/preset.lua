RegisterServerEvent("works:lenhador")
AddEventHandler("works:lenhador", function(Number)
    local source = source
    local Passport = vRP.Passport(source)
    if Passport then
        local Model = vRP.ModelPlayer(source)

        if Model == "mp_m_freemode_01" or Model == "mp_f_freemode_01" then
            TriggerClientEvent("skinshop:Apply", source, kaduzerawork["5"][Model])
			TriggerClientEvent('Notify', source, 'verde', 'Você vestiu o Traje do Lenhador', 5000)
        end
    end
end)
